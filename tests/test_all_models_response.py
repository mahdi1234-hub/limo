# ── Chat with temperature/options per model ──────────────────────

@pytest.mark.parametrize(
    "model_name,user_prompt,expected_fragment",
    MODEL_TEST_CASES,
    ids=[m[0] for m in MODEL_TEST_CASES],
)
def test_chat_with_options_per_model(
    client, mock_ollama, model_name, user_prompt, expected_fragment
):
    """Each model should handle temperature and max_tokens options."""
    mock_ollama.chat.return_value = {
        "message": {
            "role": "assistant",
            "content": f"Precise response from {model_name}: {expected_fragment}.",
        },
        "prompt_eval_count": 8,
        "eval_count": 10,
    }

    r = client.post(
        "/v1/chat/completions",
        json={
            "model": model_name,
            "messages": [
                {"role": "system", "content": "You are a helpful assistant."},
                {"role": "user", "content": user_prompt},
            ],
            "temperature": 0.3,
            "max_tokens": 100,
            "top_p": 0.9,
        },
    )

    assert r.status_code == 200
    data = r.json()
    assert data["model"] == model_name
    assert expected_fragment in data["choices"][0]["message"]["content"]

    # Verify options were passed through
    call_kwargs = mock_ollama.chat.call_args
    options = call_kwargs[1].get("options") or {}
    assert options.get("temperature") == 0.3
    assert options.get("num_predict") == 100
    assert options.get("top_p") == 0.9


# ── Multi-turn conversation per model ────────────────────────────

@pytest.mark.parametrize(
    "model_name",
    [m[0] for m in MODEL_TEST_CASES],
    ids=[m[0] for m in MODEL_TEST_CASES],
)
def test_multi_turn_chat_per_model(client, mock_ollama, model_name):
    """Each model should handle multi-turn conversations correctly."""
    mock_ollama.chat.return_value = {
        "message": {
            "role": "assistant",
            "content": f"{model_name} remembers context: The previous topic was discussed.",
        },
        "prompt_eval_count": 25,
        "eval_count": 12,
    }

    r = client.post(
        "/v1/chat/completions",
        json={
            "model": model_name,
            "messages": [
                {"role": "system", "content": "You are a helpful assistant."},
                {"role": "user", "content": "Tell me about quantum computing."},
                {"role": "assistant", "content": "Quantum computing uses qubits..."},
                {"role": "user", "content": "Can you elaborate on qubits?"},
            ],
        },
    )

    assert r.status_code == 200
    data = r.json()
    assert data["model"] == model_name
    assert len(data["choices"]) == 1
    assert data["choices"][0]["message"]["role"] == "assistant"

    # Verify all messages were forwarded to Ollama
    call_args = mock_ollama.chat.call_args
    messages = call_args[0][1]
    assert len(messages) == 4


