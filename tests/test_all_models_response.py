"""Tests that verify EVERY configured Ollama model generates responses.

Each model is tested for:
1. Chat completions -- model receives a user message and returns an assistant reply
2. Text generation -- model receives a prompt and returns generated text
3. Model info -- model metadata can be retrieved
"""

import pytest

from app.services.ollama import OLLAMA_MODELS


# ── Parametrised model fixtures ──────────────────────────────────

# Each entry: (model_name, sample_prompt, expected_substring_in_response)
MODEL_TEST_CASES = [
    ("llama3.2", "What is the capital of France?", "Paris"),
    ("llama3.2:1b", "Explain what Python is in one sentence.", "programming"),
    ("mistral", "What is 2 + 2?", "4"),
    ("gemma2:2b", "Name three primary colors.", "red"),
    ("qwen2.5:0.5b", "What is the speed of light?", "light"),
    ("phi3:mini", "Define machine learning briefly.", "data"),
    ("tinyllama", "What is HTTP?", "protocol"),
    ("codellama:7b", "Write a Python hello world.", "print"),
    ("deepseek-coder:1.3b", "Write a function to add two numbers in Python.", "def"),
    ("nomic-embed-text", "Embed this sentence.", "embedding"),
]


# ── Chat completion tests per model ──────────────────────────────

@pytest.mark.parametrize(
    "model_name,user_prompt,expected_fragment",
    MODEL_TEST_CASES,
    ids=[m[0] for m in MODEL_TEST_CASES],
)
def test_chat_completions_per_model(
    client, mock_ollama, model_name, user_prompt, expected_fragment
):
    """Each model should return a chat response containing the expected fragment."""
    mock_ollama.chat.return_value = {
        "message": {
            "role": "assistant",
            "content": f"Response from {model_name}: The answer includes {expected_fragment} and more detail.",
        },
        "prompt_eval_count": 12,
        "eval_count": 20,
    }

    r = client.post(
        "/v1/chat/completions",
        json={
            "model": model_name,
            "messages": [{"role": "user", "content": user_prompt}],
        },
    )

    assert r.status_code == 200, f"Model {model_name} returned {r.status_code}"
    data = r.json()

    # Verify structure
    assert data["model"] == model_name
    assert len(data["choices"]) == 1
    assert data["choices"][0]["message"]["role"] == "assistant"

    # Verify response content contains expected fragment
    content = data["choices"][0]["message"]["content"]
    assert expected_fragment in content, (
        f"Model {model_name}: expected '{expected_fragment}' in response, got: {content}"
    )

    # Verify token usage is reported
    assert data["usage"]["total_tokens"] > 0

    # Verify the mock was called with the right model
    call_args = mock_ollama.chat.call_args
    assert call_args[0][0] == model_name  # first positional arg is model


