# ── Verify all configured models are accounted for ───────────────

def test_all_configured_models_have_test_cases():
    """Ensure every model in OLLAMA_MODELS has a corresponding test case."""
    tested_models = {m[0] for m in MODEL_TEST_CASES}
    configured_models = set(OLLAMA_MODELS)
    missing = configured_models - tested_models
    assert not missing, f"Models without test coverage: {missing}"


# ── Error handling per model ─────────────────────────────────────

@pytest.mark.parametrize(
    "model_name",
    [m[0] for m in MODEL_TEST_CASES],
    ids=[m[0] for m in MODEL_TEST_CASES],
)
def test_chat_error_per_model(client, mock_ollama, model_name):
    """Each model should return 502 when Ollama fails."""
    mock_ollama.chat.side_effect = Exception(f"{model_name} inference timeout")

    r = client.post(
        "/v1/chat/completions",
        json={
            "model": model_name,
            "messages": [{"role": "user", "content": "test"}],
        },
    )

    assert r.status_code == 502
    assert model_name in r.json()["detail"]


